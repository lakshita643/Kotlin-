fun main(){
    var a=20
    println("Value of a is = $a")
    
    println("After increment = ${a++}")//in this case first value is stored and then value is increse
    println(a)//After increing value
    println("After decrement = ${a--}")//in this case first value is stored and then decrese
    println(a)//After decrese value
    
    println(++a)//in this case first value is increse and then stored the value
    
    println(--a)//in this case first value is decrese and then stored the value
    

}